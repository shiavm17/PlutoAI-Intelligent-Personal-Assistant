@app.route('/api/tts')
def tts_stream():
    """Stream TTS audio generated by edge-tts in real-time"""
    text = request.args.get('text', '').strip()
    if not text:
        return jsonify({'error': 'No text provided'}), 400
        
    try:
        import edge_tts
        import asyncio
        
        voice = env_vars.get("AssistantVoice", "en-IN-PrabhatNeural")
        
        loop = asyncio.new_event_loop()
        asyncio.set_event_loop(loop)
            
        async def get_audio_data():
            communicate = edge_tts.Communicate(text, voice, pitch='+5Hz', rate='+13%')
            audio_bytes = b""
            async for chunk in communicate.stream():
                if chunk["type"] == "audio":
                    audio_bytes += chunk["data"]
            return audio_bytes
            
        try:
            audio_data = loop.run_until_complete(get_audio_data())
        finally:
            loop.close()
            
        return Response(audio_data, mimetype="audio/mpeg")
    except Exception as e:
        print(f"[Error] TTS generation failed: {e}")
        return jsonify({'error': str(e)}), 500


