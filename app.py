import json
import threading
from datetime import datetime
from flask import Flask, render_template, request, jsonify, send_from_directory, Response
from flask_cors import CORS
from dotenv import dotenv_values

# Add project paths
sys.path.append(os.path.dirname(__file__))
sys.path.append(os.path.join(os.path.dirname(__file__), 'Backend'))

from Backend.botcore import BotCore

# Initialize Flask app
app = Flask(__name__, 
           template_folder=os.path.join(os.path.dirname(__file__), 'Frontend'),
           static_folder=os.path.join(os.path.dirname(__file__), 'Frontend', 'Graphics'))

CORS(app)

# Load environment variables
env_vars = dotenv_values(".env")
USERNAME = env_vars.get("Username", "User")
ASSISTANT_NAME = env_vars.get("Assistantname", "Assistant")

# Initialize BotCore instance
bot_instance = BotCore(gui_mode=True)
bot_instance.running = True

# Chat history storage
chat_history = []
chat_history_file = os.path.join(os.path.dirname(__file__), 'Data', 'web_chat_history.json')


def load_chat_history():
    """Load chat history from file"""
    try:
        if os.path.exists(chat_history_file):
            with open(chat_history_file, 'r', encoding='utf-8') as f:
                return json.load(f)
    except Exception as e:
        print(f"[Error] Loading chat history: {e}")
    return []


def save_chat_history():
    """Save chat history to file"""
    try:
        os.makedirs(os.path.dirname(chat_history_file), exist_ok=True)
        with open(chat_history_file, 'w', encoding='utf-8') as f:
            json.dump(chat_history, f, indent=2, ensure_ascii=False)
    except Exception as e:
        print(f"[Error] Saving chat history: {e}")

# Register voice mode message synchronization callback
def on_bot_voice_message(user_msg, bot_msg):
    from datetime import datetime
    chat_entry = {
        'timestamp': datetime.now().isoformat(),
        'user': user_msg,
        'bot': bot_msg
    }
    chat_history.append(chat_entry)
    if len(chat_history) > 1000:
        chat_history.pop(0)
    save_chat_history()

bot_instance.register_message_callback(on_bot_voice_message)


